let currentLang = localStorage.getItem('crm_lang') || 'en';

function t(key, vars) {
  let str = (LANGS[currentLang] && LANGS[currentLang][key]) || LANGS.en[key] || key;
  if (vars) Object.entries(vars).forEach(([k, v]) => { str = str.replace(`{${k}}`, v); });
  return str;
}

function applyLang() {
  document.documentElement.lang = currentLang;
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const icon = el.querySelector('[data-lucide], svg');
    if (icon) {
      // preserve icon, update only trailing text node
      Array.from(el.childNodes).forEach(n => { if (n.nodeType === 3) n.remove(); });
      el.append(document.createTextNode(t(el.dataset.i18n)));
    } else {
      el.textContent = t(el.dataset.i18n);
    }
  });
  document.querySelectorAll('[data-i18n-ph]').forEach(el => {
    el.placeholder = t(el.dataset.i18nPh);
  });
  document.querySelectorAll('.lang-btn').forEach(btn => {
    btn.classList.toggle('lang-active', btn.dataset.lang === currentLang);
  });
  document.dispatchEvent(new CustomEvent('langchange'));
}

function setLang(lang) {
  currentLang = lang;
  localStorage.setItem('crm_lang', lang);
  applyLang();
}

// Auto-apply on load
document.addEventListener('DOMContentLoaded', applyLang);
